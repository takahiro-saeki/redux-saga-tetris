export function updateCell(board, x, y, cell) {
  return [
    ...board.slice(0, y),
    [
      ...board[y].slice(0, x),
      cell,
      ...board[y].slice(x+1, board[y].length),
    ],
    ...board.slice(y + 1, board.length)
  ];
}

export function getCell(board, x, y) {
  return board[y][x];
}
